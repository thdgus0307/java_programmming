package techit;

public class Constants {

    public static void main(String[] args) {

        // 상수(constants, final variables)
        // 한 번 값이 할당 => 재할당 불가능
        // 전체를 대문자, 단어간 구분은 _

        final double PI_VALUE = 3.14; // final 키워드를 붙여주면됨.
        System.out.println(PI_VALUE);

        //PI_VALUE = 3.141592; error

    }
}
